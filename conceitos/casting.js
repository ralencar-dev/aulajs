//Casting -> processo de converter os tipos de dados de uma variavel.
/** Dois tipos 
 * parseInt() -> converter para um inteiro
 * parseFloat() -> converter para um flutuante
 */

let nota, nota2, Soma
//comando estrada de dados
nota = parseInt( prompt("Digite sua nota"))
nota2 = parseInt( prompt("Digite sua nota"))

//comando saida de dados
document.write(Soma = nota + nota2) //escreve na tela
console.log(nota)
console.log(nota2)

//Saida dp processamento
